@app.route("/api/admin/login", methods=["POST"])
def admin_login():
    def handler():
        payload = request.get_json(silent=True) or {}

        username = str(payload.get("username", "")).strip()
        password = str(payload.get("password", "")).strip()

        if not username or not password:
            return jsonify({
                "ok": False,
                "message": "Username and password are required.",
            }), 400

        admin_row = admins_db.find_one("username", username)

        if not admin_row:
            return jsonify({
                "ok": False,
                "message": "Invalid username or password.",
            }), 401

        stored_password = (
            admin_row.get("password_hash")
            or admin_row.get("password")
            or ""
        )

        if not _password_matches(password, stored_password):
            return jsonify({
                "ok": False,
                "message": "Invalid username or password.",
            }), 401

        return jsonify({
            "ok": True,
            "admin": admin_from_db(admin_row),
        })

    return _safe_json(handler)


