@app.route("/api/products", methods=["GET", "POST", "DELETE"])
def products_route():
    def handler():
        if request.method == "GET":
            return jsonify({"products": products.get_all()})

        if request.method == "POST":
            payload = request.get_json(silent=True) or {}
            result = products.create_product_entry(payload)
            return jsonify(result), (200 if result.get("ok") else 400)

        result = products.clear_products()
        return jsonify(result), (200 if result.get("ok") else 400)

    return _safe_json(handler)


@app.route("/api/products/<string:product_id>", methods=["PATCH", "DELETE"])
def product_entry_route(product_id):
    def handler():
        if request.method == "PATCH":
            payload = request.get_json(silent=True) or {}

            full_replace_keys = [
                "pricing",
                "name",
                "description",
                "images",
                "brandID",
                "brandId",
                "brand_id",
                "createdAt",
                "updatedAt",
                "reviews",
                "cartAddedCount",
            ]

            if any(key in payload for key in full_replace_keys):
                result = products.replace_product_entry(product_id, payload)
            else:
                result = products.update_product(product_id, payload)

            return jsonify(result), (200 if result.get("ok") else 404)

        result = products.delete_product_entry(product_id)
        return jsonify(result), (200 if result.get("ok") else 404)

    return _safe_json(handler)


