def _safe_json(handler):
    try:
        return handler()
    except Exception as error:
        return jsonify({
            "ok": False,
            "message": str(error),
        }), 500


def _password_matches(raw_password: str, stored_password: str) -> bool:
    """
    Supports two cases:
    1. Plain text while developing, example: --admin
    2. Werkzeug hash later, example generated by generate_password_hash()
    """

    raw_password = str(raw_password or "")
    stored_password = str(stored_password or "")

    if not raw_password or not stored_password:
        return False

    if stored_password == raw_password:
        return True

    try:
        return check_password_hash(stored_password, raw_password)
    except Exception:
        return False


