@app.route("/api/images/upload", methods=["POST"])
def upload_image():
    """
    Render/Supabase version:
    This endpoint now accepts image URLs.

    JSON example:
    {
        "folder": "product",
        "url": "https://res.cloudinary.com/.../image.png"
    }

    Form-data example:
    folder=product
    url=https://res.cloudinary.com/.../image.png

    If frontend still sends a file, img.py will reject local upload
    and explain that hosted image URL is required.
    """

    payload = request.get_json(silent=True) or {}

    folder = (
        payload.get("folder")
        or request.form.get("folder")
        or "product"
    )

    image_url = (
        payload.get("url")
        or payload.get("imageUrl")
        or request.form.get("url")
        or request.form.get("imageUrl")
        or ""
    )

    file = request.files.get("file")

    result = img.save(
        file=file,
        folder=folder,
        url=image_url,
    )

    return jsonify(result), (200 if result.get("ok") else 400)


